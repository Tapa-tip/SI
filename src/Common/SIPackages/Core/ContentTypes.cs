    /// Audio type.
    /// </summary>
    public const string Audio = "audio";

    /// <summary>
    /// Video type.
    /// </summary>
    public const string Video = "video";

    /// <summary>
    /// HTML type.
    /// </summary>
    public const string Html = "html";
}
